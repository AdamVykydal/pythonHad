import pygame

class MenuConst:
    def normalFont(self):
        return pygame.font.SysFont("arialBlack", 40)
    def bigFont(self):
        return pygame.font.SysFont("arialBlack", 80, bold=True)
    def plusMinusFont(self):
        return pygame.font.SysFont("arialBlack", 60)
    def smallFont(self):
        return pygame.font.SysFont("arialBlack", 25)
    def primaryColour(self):
        return (255, 255, 255)
    def secondaryColour(self):
        return (0, 0, 255)
    
    